#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	char	*d;

	d = dest;
	while (*d != '\0')
		d++;
	while (*src != '\0')
	{
		*d = (char)(*src);
		d++;
		src++;
	}
	*d = '\0';
	return (dest);
}
